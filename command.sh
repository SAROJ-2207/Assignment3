$pwd   :- Present working Directory

$touch :- create a file

$ls    :- display file

$clear :- Clear the screen

$mkdir :- Create a Directory

$cd ../:- Move to previous Directory

$cd .  :- Same directory / Present Directory

$ls .. :- Access all file previous directory

$ls -a :- It shows all files with hidden files

$cd ~  :- root directory

$cp    :- Copy file

$rm -r :- This command not check folder is empty or not directly deleted

$rmdir :- Delete only empty folder

$grep  :- It will show releted data

